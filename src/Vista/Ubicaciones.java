    public final void listarUbicaciones (){
        
        modelo.setNumRows(0);
        
        int i = 0;
        
        String datos [] = new String[3];
        
        for (Nodos nodo : VistaPrincipal.getNodos()) {
            
            datos[0] = i+"";
            datos[1] = nodo.getNombre();
            datos[2] = nodo.getPrioridad()+"";
            i++;
            modelo.addRow(datos);
            
        }
        
    }
    
